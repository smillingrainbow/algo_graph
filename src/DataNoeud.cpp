{
  int i;

  colorOnFace = true;
  points.Clear();
  pointColors.Clear();
  faces.Clear();
  faceColors.Clear();
  for (i = 0; i < noeud_nbpoints; ++i )
    points.PushBack(Coord3D(noeud_points[i][0], noeud_points[i][1],
        noeud_points[i][2]) );
  for (i = 0; i < noeud_nbfaces; ++i )
    faces.PushBack(Face(noeud_faces[i][0], noeud_faces[i][1],
        noeud_faces[i][2]) );
  for (i = 0; i < noeud_nbfaceColors; ++i )
    faceColors.PushBack(Color(noeud_faceColors[i][0],
        noeud_faceColors[i][1], noeud_faceColors[i][2]) );

  CenterObjectToOrigin();

  for (i = 0; i < points.size; ++i )
    pointColors.PushBack(Color((unsigned char)(rand() % 256), (unsigned char)(rand() % 256), (unsigned char)(rand() % 256)) );
}
