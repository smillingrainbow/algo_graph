void Drawable::LoadPredefinedSphere()
{
  int i;

  colorOnFace = true;
  points.Clear();
  pointColors.Clear();
  faces.Clear();
  faceColors.Clear();
  for (i = 0; i < sphere_nbpoints; ++i )
    points.PushBack(Coord3D(sphere_points[i][0], sphere_points[i][1],
        sphere_points[i][2]) );
  for (i = 0; i < sphere_nbfaces; ++i )
    faces.PushBack(Face(sphere_faces[i][0], sphere_faces[i][1],
        sphere_faces[i][2]) );
  for (i = 0; i < sphere_nbfaceColors; ++i )
    faceColors.PushBack(Color(sphere_faceColors[i][0],
        sphere_faceColors[i][1], sphere_faceColors[i][2]) );

  CenterObjectToOrigin();

  for (i = 0; i < points.size; ++i )
    pointColors.PushBack(Color((unsigned char)(rand() % 256), (unsigned char)(rand() % 256), (unsigned char)(rand() % 256)) );
}
