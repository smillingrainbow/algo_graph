void Drawable::LoadPredefinedTeapot()
{
  int i;

  colorOnFace = true;
  points.Clear();
  pointColors.Clear();
  faces.Clear();
  faceColors.Clear();
  for (i = 0; i < teapot_nbpoints; ++i )
    points.PushBack(Coord3D(teapot_points[i][0], teapot_points[i][1],
        teapot_points[i][2]) );
  for (i = 0; i < teapot_nbfaces; ++i )
    faces.PushBack(Face(teapot_faces[i][0], teapot_faces[i][1],
        teapot_faces[i][2]) );
  for (i = 0; i < teapot_nbfaceColors; ++i )
    faceColors.PushBack(Color(teapot_faceColors[i][0],
        teapot_faceColors[i][1], teapot_faceColors[i][2]) );

  CenterObjectToOrigin();

  for (i = 0; i < points.size; ++i )
    pointColors.PushBack(Color((unsigned char)(rand() % 256), (unsigned char)(rand() % 256), (unsigned char)(rand() % 256)) );
}
