void Drawable::LoadPredefinedComprime()
{
  int i;

  colorOnFace = true;
  points.Clear();
  pointColors.Clear();
  faces.Clear();
  faceColors.Clear();
  for (i = 0; i < comprime_nbpoints; ++i )
    points.PushBack(Coord3D(comprime_points[i][0], comprime_points[i][1],
        comprime_points[i][2]) );
  for (i = 0; i < comprime_nbfaces; ++i )
    faces.PushBack(Face(comprime_faces[i][0], comprime_faces[i][1],
        comprime_faces[i][2]) );
  for (i = 0; i < comprime_nbfaceColors; ++i )
    faceColors.PushBack(Color(comprime_faceColors[i][0],
        comprime_faceColors[i][1], comprime_faceColors[i][2]) );

  CenterObjectToOrigin();

  for (i = 0; i < points.size; ++i )
    pointColors.PushBack(Color((unsigned char)(rand() % 256), (unsigned char)(rand() % 256), (unsigned char)(rand() % 256)) );
}
